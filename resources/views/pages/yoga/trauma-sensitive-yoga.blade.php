                <p>
                    Leading trauma theorists recognize that because trauma profoundly affects the body and many symptoms are
                    somatically based, trauma treatment must incorporate the body. Neuroscience informs us that there is a
                    part of the brain called the insula, which transmits our physical, bodily sensations into our conscious
                    awareness. Brain imaging studies repeatedly show a marked decrease in activation of the insula, and
                    other parts of the brain that are related to self-awareness in people who have experienced complex
                    trauma. Only by activating this dormant part of the brain can we begin to truly feel that we are here
                    now, feeling something in the present moment and not reliving a traumatic event from the past.
                </p>
                <p>
                    In recent years, Trauma Sensitive Yoga has emerged as an effective adjunctive treatment for trauma
                    survivors because it naturally builds interoception – or the ability to sense how something feels in our
                    body. The practice of yoga incorporates a combination of yoga poses, mindfulness and breathing
                    techniques to cultivate a connection to our feeling body. When we move mindfully from yoga pose to yoga
                    pose and focus our attention on our breath and sensations that occur in our body, we begin to notice the
                    connection between our body and our emotions. For example, when we hold a pose for more than a few
                    breaths, we may notice sensation start to build in our legs. We then investigate how focusing on our
                    exhalation breath can produce a sense of calm, or maybe we will feel more ease if we shift the position
                    of our feet. We also notice how sensations have a beginning, a peak, and an end. Knowing that sensations
                    come to an end can expand our capacity for dealing with physical and emotional distress. Noticing and
                    tolerating inner experiences in the present moment begins the process of reclaiming the body and
                    building resilience – helping us to develop a new relationship with our inner sensory world.
                </p>
                <p>
                    In a Trauma Sensitive Yoga class, you will be encouraged to practice making empowering choices with your
                    body based on what you are feeling in the present moment – helping you to explore and connect to your
                    body in a safe supportive environment. The language used in a Trauma Sensitive Yoga class is grounded in
                    an empathetic understanding of the physical, psychological, and emotional effects of trauma. The goal is
                    to connect to the somatic body, and then navigate from that entry point to compassionately addressing
                    the emotions and the cognitions that arise as a result. The yoga poses we move through may help to
                    release physical tension and trapped psychological energy, whilst focusing on natural breathing to help
                    promote relaxation, mindfulness, and embodiment. By incorporating the mind, the body and the spirit into
                    treatment, we’re offered an opportunity to heal using our innate resources.
                </p>
