                <p>
                    As Bessel van der Kolk describes in his quote, emotional pain and traumatic memories can be held in the
                    body long after a traumatic experience has occurred. For some of us, it can feel like the trauma is
                    remembered much less in words and more so with feelings that occur in the body. These bodily sensations
                    associated with trauma can overwhelm the nervous system and our coping skills, which can result in a
                    profound and lasting sense of vulnerability and a lack of safety and control. As Bessel van der Kolk
                    aptly describes it… “the body keeps the score”.
